namespace Exemplo1;

class Program
{
    static void Main(string[] args)
    {
        for (int i = 0; i <= 5; i++)
        {
            Console.WriteLine(i);
        }
        
        Console.WriteLine();
        
        for (int j = 5; j >= 0; j--)
        {
            Console.WriteLine(j);
        }
    }
}